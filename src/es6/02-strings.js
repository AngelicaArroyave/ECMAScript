// Antes de ES6
let hello = 'Hello'
let world = 'World'
let epicPhrase = hello + ' ' + world + '!'
console.log(epicPhrase); // Hello World!

// Template literals o Plantillas literales
let epicPhrase2 = `${hello} ${world}!`
console.log(epicPhrase2); // Hello World!

// Multi-line strings
let lorem = 'Esto es un string \n ' + 'Esta es otra línea'
console.log(lorem);

let lorem2 = `Esta es una frase epica
    esta es la continuación de esa frase epica`
console.log(lorem2);