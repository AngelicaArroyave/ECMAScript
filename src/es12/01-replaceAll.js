// Replace All
const cadena = 'JavaScript es un lenguaje de programación'

const replaceCadena = cadena.replace('JavaScript', 'TypeScript')
console.log(replaceCadena); // TypeScript es un lenguaje de programación

const newCadena = 'JavaScript es maraviloso, con JavaScript puedo crear el futuro de la web'

const replaceAllCadena = newCadena.replaceAll('JavaScript', 'TypeScript')
console.log(replaceAllCadena); // TypeScript es maraviloso, con TypeScript puedo crear el futuro de la web